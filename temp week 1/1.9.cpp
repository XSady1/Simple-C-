#include <iostream>
using namespace std;

int main() {
    double side = 5.2;
    double area = side * side;
    double perimeter = 4 * side;

    cout << "For a square with a side length of " << side << ":" << endl;
    cout << "Area = " << area << endl;
    cout << "Perimeter = " << perimeter << endl;

    return 0;
}
